@dataclass(frozen=True)
class ExerciseFormMediaSeed:
    canonical_exercise_name: str
    source_exercise_id: str
    source_url: str
    asset_path: str
    media_key: str
    alt_text: str
    caption: str
    sort_order: int
    asset_sha256: str


def _image_pair(
    canonical_exercise_name: str,
    source_exercise_id: str,
    slug: str,
    start_hash: str,
    finish_hash: str,
) -> tuple[ExerciseFormMediaSeed, ExerciseFormMediaSeed]:
    source_root = (
        "https://raw.githubusercontent.com/yuhonas/free-exercise-db/main/exercises/"
        f"{source_exercise_id}"
    )
    public_root = f"/exercise-media/free-exercise-db/{slug}"
    return (
        ExerciseFormMediaSeed(
            canonical_exercise_name=canonical_exercise_name,
            source_exercise_id=source_exercise_id,
            source_url=f"{source_root}/0.jpg",
            asset_path=f"{public_root}/start.jpg",
            media_key="start",
            alt_text=f"{canonical_exercise_name} starting position",
            caption="Start",
            sort_order=1,
            asset_sha256=start_hash,
        ),
        ExerciseFormMediaSeed(
            canonical_exercise_name=canonical_exercise_name,
            source_exercise_id=source_exercise_id,
            source_url=f"{source_root}/1.jpg",
            asset_path=f"{public_root}/finish.jpg",
            media_key="finish",
            alt_text=f"{canonical_exercise_name} finishing position",
            caption="Finish",
            sort_order=2,
            asset_sha256=finish_hash,
        ),
    )


